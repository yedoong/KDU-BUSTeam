package bus;

public class Bus {
	private String bus_day;
	private String bus_inout;
	private String bus_time_start;
	private String bus_time_end;
	private String bus_number;
	private String bus_state;
	private String bus_passenger;
	
	public String getBus_day() {
		return bus_day;
	}
	public void setBus_day(String bus_day) {
		this.bus_day = bus_day;
	}
	public String getBus_inout() {
		return bus_inout;
	}
	public void setBus_inout(String bus_inout) {
		this.bus_inout = bus_inout;
	}
	public String getBus_time_start() {
		return bus_time_start;
	}
	public void setBus_time_start(String bus_time_start) {
		this.bus_time_start = bus_time_start;
	}
	public String getBus_time_end() {
		return bus_time_end;
	}
	public void setBus_time_end(String bus_time_end) {
		this.bus_time_end = bus_time_end;
	}
	public String getBus_number() {
		return bus_number;
	}
	public void setBus_number(String bus_number) {
		this.bus_number = bus_number;
	}
	public String getBus_state() {
		return bus_state;
	}
	public void setBus_state(String bus_state) {
		this.bus_state = bus_state;
	}
	public String getBus_passenger() {
		return bus_passenger;
	}
	public void setBus_passenger(String bus_passenger) {
		this.bus_passenger = bus_passenger;
	}
	
}
